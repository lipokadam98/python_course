# useful link https://docs.python.org/3/tutorial/datastructures.html
states_of_america = ['Delaware', 'Florida', 'Pennsylvania', 'South Dakota']

# Append adds a new item to the list
states_of_america.append('Adamland')

# Extend adds a new array to the existing array
states_of_america.extend(['Adamland', 'Newland', 'Etc'])

print(states_of_america)

# nested lists
fruits = ['Strawberries', 'Nectarines', 'Apples', 'Grapes', 'Peaches', 'Cherries', 'Pears']
vegetables = ['Spinach', 'Kale', 'Tomatoes', 'Celery', 'Potatoes']

dirty_dozen = [fruits, vegetables]

print(dirty_dozen)
